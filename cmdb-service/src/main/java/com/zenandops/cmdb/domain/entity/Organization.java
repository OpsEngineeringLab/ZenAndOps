package com.zenandops.cmdb.domain.entity;

import com.zenandops.cmdb.domain.vo.OrganizationType;

import java.time.Instant;

/**
 * Hierarchical organizational unit (ROOT, BUSINESS_UNIT, DEPARTMENT, TEAM).
 * Designed as a mutable class for MongoDB Panache compatibility.
 */
public class Organization {

    private String id;
    private String name;
    private OrganizationType type;
    private String parentId;
    private String responsiblePerson;
    private String costCenter;
    private Instant createdAt;
    private Instant updatedAt;

    public Organization() {
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public OrganizationType getType() {
        return type;
    }

    public void setType(OrganizationType type) {
        this.type = type;
    }

    public String getParentId() {
        return parentId;
    }

    public void setParentId(String parentId) {
        this.parentId = parentId;
    }

    public String getResponsiblePerson() {
        return responsiblePerson;
    }

    public void setResponsiblePerson(String responsiblePerson) {
        this.responsiblePerson = responsiblePerson;
    }

    public String getCostCenter() {
        return costCenter;
    }

    public void setCostCenter(String costCenter) {
        this.costCenter = costCenter;
    }

    public Instant getCreatedAt() {
        return createdAt;
    }

    public void setCreatedAt(Instant createdAt) {
        this.createdAt = createdAt;
    }

    public Instant getUpdatedAt() {
        return updatedAt;
    }

    public void setUpdatedAt(Instant updatedAt) {
        this.updatedAt = updatedAt;
    }
}
